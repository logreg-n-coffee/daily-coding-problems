/**
 * Given two singly linked lists that intersect at some point, find the intersecting node. 
 * The lists are non-cyclical.
 * 
 * For example, given A = 3 -> 7 -> 8 -> 10 and B = 99 -> 1 -> 8 -> 10, return the node with value 8.
 * 
 * In this example, assume nodes with the same value are the exact same node objects.
 * Do this in O(M + N) time (where M and N are the lengths of the lists) and constant space.
 * 
 * Definition for singly-linked list.
 * class ListNode {
 *     constructor(val) {
 *         this.val = val;
 *         this.next = null;
 *     }
 * }
 */

// there are two cases to think about:
// 1. Two linked lists have the same length - move the pointer all the way to the end until it reaches the end
// 2. Two linked lists do not have the same length - put t

const getIntersectionNode = (headA, headB) => {
    // boundary check
    if (headA === null || headB === null) return null;

    let p1 = headA;
    let p2 = headB;

    while (p1 !== p2) {
        // for the end of first iteration, we just reset the pointer to the head of another linkedlist
        p1 = p1 === null ? headB : p1.next;
        p2 = p2 === null ? headA : p2.next;
    }

    return p1;
};

// n -> length of linked list A
// m -> length of linked list B
// time: O(n + m)
// space: O(1)

